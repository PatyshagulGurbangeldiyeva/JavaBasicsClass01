package com.syntax.class03;

public class RelationEquality {

	public static void main(String[] args) {
	
		int i=10;
		int y=20;
		
		
		System.out.println(i>y);//if i is bigger than y the result will be false
		System.out.println(i<y);// if i smaller than y; the result will be true
		System.out.println(i==y);// if i equals to y; the result will be false
		System.out.println(i!=y);// IF I ISNT equal to y; the rsult will be true
		
			//the result in comparing will be always boolean
		
		System.out.println("___________________________________________");
	
		
		double a=99.99;
		double b=10.99;
		
		boolean result1=a>b;//false
		boolean result2= a<b;//true
		
		System.out.println(result1+" "+result2);
		

	}

}
