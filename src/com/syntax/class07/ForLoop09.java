package com.syntax.class07;

public class ForLoop09 {

	public static void main(String[] args) {
		// We use for loop - when we know how many times we need to repeat the block of code
		
		/*   in fo loop we have all in one
		 * ex: for (int b=1; b<4; b++){}
		 * 
		 * in while loop we have everything seperatly:
		 * int n=4;
		 * ex: while (n<4){
		 * code;
		 * code;
		 * n++;}
		 */

		// Goodmorning 5 times ( you know how many times we need to say. so we use for loop)!!!!!!
		// while loop we use when we do not know how many times we need to enter the input!!!!!!
		
		for (int c=4; c<6; c++) {
			System.out.println("Good morning");
		}
	}

}
